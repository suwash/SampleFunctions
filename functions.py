
# Addition
def addition(a, b):
 	summation = a + b

 	return summation

# Subtraction
def subtraction(a, b):

	difference = a - b

	return difference

# Multiplication
def division(a, b):

	quotient = a / b

	return quotient

# Division

def multiplication(a, b):

	product = a * b

	return product
